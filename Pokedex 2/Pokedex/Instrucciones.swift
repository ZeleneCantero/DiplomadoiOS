//Pokédex
//
//Objetivo El objetivo de este proyecto es crear una aplicación que funcione como una Pokédex. La aplicación tendrá tres pantallas principales en una barra de pestañas:
//
//Lista de Pokémon
//
//Esta pantalla mostrará una lista de todos los Pokémon en orden numérico.
//Los datos mostrados incluirán:
//Nombre del Pokémon
//Número del Pokémon
//La pantalla también incluirá una barra de búsqueda que permitirá a los usuarios buscar por nombre o número de Pokémon.


//Al seleccionar un Pokémon de la lista, se abrirá la pantalla de detalles del Pokémon.
//Pantalla de Detalles del Pokémon
//
//Esta pantalla mostrará información detallada sobre el Pokémon seleccionado.
//Los datos mostrados incluirán:
//Imagen del Pokémon
//Nombre del Pokémon
//Número del Pokémon
//Información de la Pokédex
//Tipos del Pokémon

//Cadena evolutiva del Pokémon*
//Cuando un usuario selecciona un Pokémon de la lista, se abrirá esta pantalla de detalles del Pokémon nuevamente.
//Pantalla de Tipos de Pokémon
//
//Esta pantalla mostrará una lista de todos los tipos de Pokémon en una colección.
//Los datos mostrados incluirán:
//Nombre del tipo de Pokémon
//Símbolo del tipo de Pokémon
//Al seleccionar un tipo de la lista, se abrirá la pantalla de detalles del tipo.
//Pantalla de Detalles del Tipo
//
//Esta pantalla mostrará información sobre el tipo de Pokémon seleccionado.
//Los datos mostrados incluirán:
//Doble daño infligido
//Doble daño recibido
//Mitad de daño infligido
//Mitad de daño recibido
//No afecta a
//Sin efecto contra
//Esta pantalla mostrará la información detallada sobre el tipo de Pokémon seleccionado.
//Pantalla de Favoritos
//
//Esta pantalla mostrará una lista de Pokémon que el usuario ha marcado como favoritos.
//La lista tendrá el mismo formato que la pantalla de lista de Pokémon.
//Al seleccionar un Pokémon de la lista, se abrirá la pantalla de detalles del Pokémon.
//Los Pokémon favoritos deben almacenarse en la memoria del teléfono.


